const crypto = require('crypto');

const solution = ([input]) => {
    console.log(crypto.createHash('sha256').update(input).digest('hex'));
};

const input = [];

const rl = require('readline')
    .createInterface({
        input: process.stdin
    });

rl.on('line', line => {
    if (line === 'exit') {
        rl.close();
        return;
    }
    input.push(line)
}).on('close', () => {
    solution(input);
    process.exit();
});